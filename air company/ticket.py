class Ticket:
    def __init__(self, travel_name, move_time, name, national_id, app_code, airline_name, seat_number, orgin, destination, end_time, seat_type, price):
        self.travel_name = travel_name
        self.move_time = move_time
        self.name = name
        self.national_id = national_id
        self.app_code = app_code
        self.airline = airline_name
        self.seat_number = seat_number
        self.orgin = orgin
        self.destination = destination
        self.end_time = end_time
        self.seat_type = seat_type
        self.price = price
